    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if (txtNpm.getText().trim().isEmpty() || 
            txtNama.getText().trim().isEmpty() || 
            txtJurusan.getText().trim().isEmpty()) {
            
            JOptionPane.showMessageDialog(this, 
                "Semua field harus diisi!", 
                "Validasi", 
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Session session = NewHibernateUtil.getSessionFactory().openSession();
        Transaction transaction = null;
        
        try {
            transaction = session.beginTransaction();
            
            Mahasiswa mhs = new Mahasiswa();
            mhs.setNpm(txtNpm.getText().trim());
            mhs.setNama(txtNama.getText().trim());
            mhs.setJurusan(txtJurusan.getText().trim());
            
            session.save(mhs);
            transaction.commit();
            
            JOptionPane.showMessageDialog(this, 
                "Data berhasil disimpan!", 
                "Sukses", 
                JOptionPane.INFORMATION_MESSAGE);
            
            loadData();
            clearForm();
            
        } catch (Exception e) {
            if (transaction != null) {
                transaction.rollback();
            }
            JOptionPane.showMessageDialog(this, 
                "Error simpan data: " + e.getMessage(), 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        } finally {
            session.close();
        }

    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (txtId.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, 
            "Pilih data dari table terlebih dahulu!", 
            "Validasi", 
            JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    if (txtNpm.getText().trim().isEmpty() || 
        txtNama.getText().trim().isEmpty() || 
        txtJurusan.getText().trim().isEmpty()) {
        
        JOptionPane.showMessageDialog(this, 
            "Semua field harus diisi!", 
            "Validasi", 
            JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    Session session = NewHibernateUtil.getSessionFactory().openSession();
    Transaction transaction = null;
    
    try {
        transaction = session.beginTransaction();
        
        int id = Integer.parseInt(txtId.getText().trim());
        
        // PERBAIKAN: Cast ke Mahasiswa untuk Hibernate 4
        Mahasiswa mhs = (Mahasiswa) session.get(Mahasiswa.class, id);
        
        if (mhs != null) {
            mhs.setNpm(txtNpm.getText().trim());
            mhs.setNama(txtNama.getText().trim());
            mhs.setJurusan(txtJurusan.getText().trim());
            
            session.update(mhs);
            transaction.commit();
            
            JOptionPane.showMessageDialog(this, 
                "Data berhasil diupdate!", 
                "Sukses", 
                JOptionPane.INFORMATION_MESSAGE);
            
            loadData();
            clearForm();
            
        } else {
            JOptionPane.showMessageDialog(this, 
                "Data tidak ditemukan!", 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
        }
        
    } catch (Exception e) {
        if (transaction != null) {
            transaction.rollback();
        }
        JOptionPane.showMessageDialog(this, 
            "Error update data: " + e.getMessage(), 
            "Error", 
            JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    } finally {
        session.close();
    }

    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (txtId.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, 
            "Pilih data dari table terlebih dahulu!", 
            "Validasi", 
            JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    int confirm = JOptionPane.showConfirmDialog(this, 
        "Apakah Anda yakin ingin menghapus data ini?", 
        "Konfirmasi Hapus", 
        JOptionPane.YES_NO_OPTION);
    
    if (confirm != JOptionPane.YES_OPTION) {
        return;
    }
    
    Session session = NewHibernateUtil.getSessionFactory().openSession();
    Transaction transaction = null;
    
    try {
        transaction = session.beginTransaction();
        
        int id = Integer.parseInt(txtId.getText().trim());
        
        // PERBAIKAN: Cast ke Mahasiswa untuk Hibernate 4
        Mahasiswa mhs = (Mahasiswa) session.get(Mahasiswa.class, id);
        
        if (mhs != null) {
            session.delete(mhs);
            transaction.commit();
            
            JOptionPane.showMessageDialog(this, 
                "Data berhasil dihapus!", 
                "Sukses", 
                JOptionPane.INFORMATION_MESSAGE);
            
            loadData();
            clearForm();
            
        } else {
            JOptionPane.showMessageDialog(this, 
                "Data tidak ditemukan!", 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
        }
        
    } catch (Exception e) {
        if (transaction != null) {
            transaction.rollback();
        }
        JOptionPane.showMessageDialog(this, 
            "Error hapus data: " + e.getMessage(), 
            "Error", 
            JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    } finally {
        session.close();
    }

    }//GEN-LAST:event_btnDeleteActionPerformed

